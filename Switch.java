import java.util.*;
public class Switch {
 public static void main(String[] args)
{
  Scanner sc = new Scanner(System.in);
 int x = sc.nextInt();
switch (x) {
 case 1: System.out.println("Hello");
 break;
 case 2: System.out.println("Hii");
 default: System.out.println("Invalif");
 break;
}
 
}
}
